:- module(utils, [printList/1]).

% ----------------------------------------------------------------
% IMPORTS
% ----------------------------------------------------------------

% none

% ----------------------------------------------------------------
% printList/1
% ----------------------------------------------------------------

printList([])      :- write('').
printList([X])     :- write(X).
printList([X|P])   :- write(X), write(', '), printList(P).
